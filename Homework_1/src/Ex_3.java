//Calculate the sum and average of three integers;
public class Ex_3 {
    public static void main(String[] args) {
        int a = 2; int b = 6; int c = 9; double x;

        x = a + b + c;//the sum of 3 ints
        System.out.println("sum = " + x);

        x /= 3; //x = (a + b + c)/3; the average of 3 ints
        System.out.println("avg = " + x);
    }
}
